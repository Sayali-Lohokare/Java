class SwitchDemo{
	public static void main(String[] args){
		String str="mon";
		switch(str){
			case "mon":
				System.out.println("monday");
				break;
			case "tue":
				System.out.println("tuesday");
				break;
			case "sun":
				System.out.println("its Sunday");
				break;
		}
		System.out.println("After switch");
	}
}
