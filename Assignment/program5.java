class Unary{
	public static void main(String[] args){
		int x = 49;
	        int y = 56;
           
              System.out.println(++x);
              System.out.println(++y);
              System.out.println(x--);
              System.out.println(y--);
              System.out.println(x);
              System.out.println(y);
           }
}	

